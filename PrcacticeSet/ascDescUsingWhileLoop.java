package PrcacticeSet;

public class ascDescUsingWhileLoop {

	public static void main(String[] args) {
	 int i=1;
	 while(i<=10) {
		 System.out.println("Ascending order series:"+i);
		 i=i+1;
	 }
	 
	 int j=10;
	 while(j>=1) {
		 System.out.println("Descending order series:"+j);
		 j=j-1;
	 }

	}

}
