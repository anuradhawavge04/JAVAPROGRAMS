package PrcacticeSet;

public class ascDescUsingDoWhileLoop {

	public static void main(String[] args) {
		int i=1;
		do {
			System.out.println("Ascending order:"+i);
			i=i+1;
		}while(i<=10);
		
		
		int j=10;
		do {
			System.out.println("Descending order :"+j);
			j=j-1;
		}while(j>=1);

	}

}
