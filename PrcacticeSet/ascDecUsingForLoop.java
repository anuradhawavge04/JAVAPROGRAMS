public class ascDecUsingForLoop {

	public static void main(String[] args) {
		int n=10;
		for(int i=1;i<=10;i++)
		{
			System.out.println("Series in Ascending order:"+i);
		}
		
		for(int j=10;j>=1;j--) {
			System.out.println("Series in ascending order :"+j);
		}
	}

}
